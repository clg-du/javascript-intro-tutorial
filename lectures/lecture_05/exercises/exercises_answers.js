
// Lecture 5 Exercises - Data Types

/* In this exercise, you will practice working with different data types in
JavaScript. Remember to log output to the console to see the results of your
code. */

// Exercise 1: Declare a variable named 'num' and assign it a number value.
let num = 5;
console.log('num:', num);

// Exercise 2: Declare a variable named 'str' and assign it a string value.
let str = 'Hello, World!';
console.log('str:', str);

// Exercise 3: Declare a variable named 'bool' and assign it a boolean value.
let bool = true;
console.log('bool:', bool);

// Exercise 4: Declare a variable named 'obj' and assign it an object value.
let obj = {
  name: 'John Doe',
  age: 38,
  city: 'New York',
  country: 'United States'
};
console.log(obj);
