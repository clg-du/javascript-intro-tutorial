
// Lecture 12 - Conditional Statements

// Conditional statements are used to execute different blocks of code based on
// different conditions. The most common conditional statements in JavaScript are
// if statements, else statements, and else if statements.

// In this example, we demonstrate the use of if statements to check if a number
// is positive, negative, or zero.

// Define a number 
let num = 5;

// Check if the number is positive
if (num > 0) {
  console.log("The number is positive.");
}

// Check if the number is negative
if (num < 0) {
  console.log("The number is negative.");
}

// Check if the number is zero
if (num === 0) {
  console.log("The number is zero.");
}

// In this example, we define a number and use if statements to check if the number
// is positive, negative, or zero. The if statements execute different blocks of
// code based on the condition being evaluated.

// If else statements are used to execute a block of code if a specified condition is true,
// and another block of code if the condition is false. In JavaScript, if else statements
// are defined using the if keyword followed by a condition in parentheses, a block of code
// in curly braces, and an else keyword followed by another block of code in curly braces.

// In this example, we demonstrate the use of an if else statement to check if a number is
// positive or negative.

// Define a number
let number = 5;

// Check if the number is positive
if (number > 0) {
  console.log("The number is positive");
}
// If the number is not positive, check if it is negative
else {
  console.log("The number is not positive");
}

// In this example, we define a number and use an if else statement to check if the number
// is positive. If the number is positive, the first block of code will execute and log
// "The number is positive" to the console. If the number is not positive, the else block
// of code will execute and log "The number is not positive" to the console.

// If else statements are a powerful tool for controlling the flow of a program based on
// specified conditions. They allow you to execute different blocks of code depending on
// whether a condition is true or false.

// Conditional statements are an essential part of control flow in JavaScript and
// are used to make decisions in your code based on different conditions.

