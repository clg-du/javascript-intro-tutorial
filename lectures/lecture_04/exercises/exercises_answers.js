
// Lecture 4 Exercises - Variables

/* Variables are used to store data in a program. They are like containers that
hold values. Here are som exercises to help you practice working with variables.
Remember to log output to the console to see the results of your code. */

// Exercise 1: Declare a variable named 'name' and assign it a value of your
// name.
let myName = 'John Doe';
console.log('Name: ', myName);

// Exercise 2: Declare a variable named 'age' and assign it a value of your age.
let age = 38;
console.log('Age: ', age);

// Exercise 3: Declare a variable named 'isMarried' and assign it a value of
// true or false.
let isMarried = false;
console.log('Married: ', isMarried);

// Exercise 4: Declare a variable named 'city' and assign it a value of your
// city.
let city = 'New York';
console.log('City: ', city);

// Exercise 5: Declare a variable named 'country' and assign it a value of your
// country.
let country = 'United States';
console.log('Country: ', country);