// Exercise 1: Declare a variable named 'num1' and assign it a number value.
let num1 = 5;
console.log('The number is: ' + num1);

// Exercise 2: Declare a variable named 'num2' and assign it a number value.
let num2 = 10;
console.log('The number is: ' + num2);

// Exercise 3: Declare a variable named 'sum' and assign it the sum of 'num1'
// and 'num2'.
let sum = num1 + num2;
console.log('The sum is: ' + sum);

// Exercise 4: Declare a variable named 'difference' and assign it the
// difference of 'num1' and 'num2'.
let difference = num1 - num2;
console.log('The difference is: ' + difference);

// Exercise 5: Declare a variable named 'product' and assign it the product of
// 'num1' and 'num2'.
let product = num1 * num2;
console.log('The product is: ' + product);

// Exercise 6: Declare a variable named 'quotient' and assign it the quotient of
// 'num1' and 'num2'.
let quotient = num1 / num2;
console.log('The quotient is: ' + quotient);

// Exercise 7: Declare a variable named 'remainder' and assign it the remainder
// of 'num1' divided by 'num2'.
let remainder = num1 % num2;
console.log('The remainder is: ' + remainder);

