
// Lecture 7 - String Methods

// String methods are used to manipulate strings in JavaScript. Some of the most
// common string methods include:

// 1. toUpperCase(): Converts a string to uppercase.
let str = "hello";
console.log("str.toUpperCase(): ", str.toUpperCase());

// 2. toLowerCase(): Converts a string to lowercase.
let str = "HELLO";
console.log("str.toLowerCase(): ", str.toLowerCase());

// 3. charAt(): Returns the character at a specified index in a string.
let str = "hello";
console.log("str.charAt(0): ", str.charAt(0));

// 4. indexOf(): Returns the index of the first occurrence of a specified value in a string.
let str = "hello";
console.log("str.indexOf('e'): ", str.indexOf('e'));

// 5. lastIndexOf(): Returns the index of the last occurrence of a specified value in a string.
let str = "hello";
console.log("str.lastIndexOf('l'): ", str.lastIndexOf('l'));

// 6. slice(): Extracts a part of a string and returns a new string.
let str = "hello";
console.log("str.slice(1, 3): ", str.slice(1, 3));

// 7. substring(): Extracts a part of a string and returns a new string.
let str = "hello";
console.log("str.substring(1, 3): ", str.substring(1, 3));

// 8. split(): Splits a string into an array of substrings.
let str = "hello,world";
console.log("str.split(','): ", str.split(','));

// 9. replace(): Replaces a specified value in a string with another value.
let str = "hello";
console.log("str.replace('e', 'a'): ", str.replace('e', 'a'));

// 10. trim(): Removes whitespace from both ends of a string.
let str = " hello ";
console.log("str.trim(): ", str.trim());

