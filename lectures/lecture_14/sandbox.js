
// Lecture 14 - For Loops

// Loops are used to execute a block of code multiple times. The most common types of
// loops in JavaScript are for loops, while loops, and do...while loops.

// In this example, we demonstrate the use of a for loop to iterate over an array of
// numbers and calculate the sum of the numbers.

// Define an array of numbers
let numbers = [1, 2, 3, 4, 5];

// Initialize a variable to store the sum
let sum = 0;

// Use a for loop to iterate over the array of numbers
for (let i = 0; i < numbers.length; i++) {
  // Add the current number to the sum
  sum += numbers[i];
}

// Log the sum to the console
console.log("sum: ", sum);

// In this example, we define an array of numbers and use a for loop to iterate over
// the array. The for loop uses an index variable i to access each element of the
// array and calculate the sum of the numbers.

// For loops are a powerful tool for iterating over arrays, strings, and other data
// structures in JavaScript. They provide a concise and expressive way to repeat
// a block of code multiple times.

