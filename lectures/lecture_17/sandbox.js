
// Lecture 17 - If Statements

// If statements are used to execute a block of code if a specified condition is true.
// In JavaScript, if statements are defined using the if keyword followed by a condition
// in parentheses and a block of code in curly braces.

// In this example, we demonstrate the use of an if statement to check if a number is
// positive or negative.

// Define a number
let num = -5;

// Check if the number is positive
if (num > 0) {
  console.log("The number is positive");
}

// Check if the number is negative
if (num < 0) {
  console.log("The number is negative");
}

// In this example, we define a number and use two if statements to check if the number
// is positive or negative. If the number is positive, the first if statement will
// execute and log "The number is positive" to the console. If the number is negative,
// the second if statement will execute and log "The number is negative" to the console.

// If statements are a fundamental concept in JavaScript and are used to control the flow
// of a program based on specified conditions.

