
// Lecture 13 - Functions

// Functions are reusable blocks of code that perform a specific task. In JavaScript,
// functions are defined using the function keyword followed by a name, a list of
// parameters in parentheses, and a block of code in curly braces.

// In this example, we define a simple function that takes two parameters and returns
// the sum of the two numbers.

// Define a function called add
function add(a, b) {
  return a + b;
}

// Call the add function with two numbers
let result = add(5, 3);

// Log the result to the console
console.log("result: ", result);

// In this example, we define a function called add that takes two parameters, a and b.
// The function returns the sum of the two numbers. We then call the add function with
// the arguments 5 and 3, which returns the result 8.

// Functions are a fundamental concept in JavaScript and are used to organize and
// encapsulate code into reusable blocks. They are an essential part of writing
// maintainable and scalable code.

